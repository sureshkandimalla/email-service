package com.srk.springboot.emailservice.model;

public class EmailUser {
    String name;
    String emailId;

    public EmailUser(){}
    public EmailUser(String name, String emailId) {
        this.name = name;
        this.emailId = emailId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmailId() {
        return emailId;
    }

    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }
}
